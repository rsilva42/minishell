#include "libft.h"

void	ft_strclr(char *s)
{
	size_t i;
	size_t len;

	if (s)
	{
		i = 0;
		len = ft_strlen(s);
		while (i <= len)
		{
			s[i] = '\0';
			i++;
		}
	}
}
