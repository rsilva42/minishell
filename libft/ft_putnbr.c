#include "libft.h"

void	ft_putnbr(int n)
{
	long val;

	val = n;
	if (val < 0)
	{
		ft_putchar('-');
		val = -val;
	}
	if (val / 10 > 0)
		ft_putnbr(val / 10);
	ft_putchar(val % 10 + 48);
}
